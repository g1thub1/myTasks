package homework.l2;

public class Literals {

    public static void main(String[] args) {
        System.out.println('\n');
        System.out.println("Логический литерал \t" + true);
        System.out.println("Строковый литерал");
        System.out.println("Символьный литерал \t" + 'z');

        System.out.println("Целочисленный 2-й \t" + 0b101);
        System.out.println("Целочисленный 8-й \t" + 07);
        System.out.println("Целочисленный 10-й \t" + 878);

        System.out.println("Целочисленный 16-й \t" + 0x0005);
        System.out.println("Литерал типа float \t" + 78.9f);
        System.out.println("Литерал типа double\t" + 78.9);
    }
}
